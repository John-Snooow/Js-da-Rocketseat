// metodos para trabelhar com data e horas

let date = new Date("July 3, 2025 22:50:00")

//modificando o ano
date.setFullYear(2026)

//modificar o mês (começa com 0)
date.setMonth(9)

//modificando o dia
date.setDate(15)

//modificando a hora
date.setHours(10)

//modificando os minutos
date.setMinutes(30)

//modificando os segundos
date.setSeconds(0)

console.log(date) // data e hora